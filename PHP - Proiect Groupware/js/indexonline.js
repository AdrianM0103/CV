$(document).ready(function(){
    $(".adaugaProiectForm").hide();
    $(".adaugaLista").hide();
    $(".stergeLista").hide();
    $(".adaugaSarcina").hide();
    $(".stergeSarcina").hide();
    $(".adaugaMembru").hide();
    $(".stergeMembru").hide();
    $(".stergeProiect").hide();
    $(".adaugaAdminSecundar").hide();
    $(".stergeAdminSecundar").hide();
    $(".exportRaport").hide();
    
    $("#addLista").click(function afisAddLista(){
        if(listaadd==0)
            adaugaListaShow();
    });
    
    $(".cancelAddLista").click(function ascuAddLista(){
        if(listaadd==1)
            adaugaListaHide();
    })
    
    $("#delLista").click(function afisDelLista(){
        if(listadel==0)
            stergeListaShow();
    });
    
    $(".cancelDelLista").click(function ascuDelLista(){
        if(listadel==1)
            stergeListaHide();
    })
    
     $("#addSarcina").click(function afisAddLista(){
        if(sarcinaadd==0)
            adaugaSarcinaShow();
    });
    
    $(".cancelAddSarcina").click(function ascuAddLista(){
        if(sarcinaadd==1)
            adaugaSarcinaHide();
    })
    
    $("#delSarcina").click(function afisDelSarcina(){
        if(sarcinadel==0)
            stergeSarcinaShow();
    });
    
    $(".cancelDelSarcina").click(function ascuDelSarcina(){
        if(sarcinadel==1)
            stergeSarcinaHide();
    })
    
     $("#addMembru").click(function afisAddMember(){
        if(memberadd==0)
            adaugaMembruShow();
    });
    
    $(".cancelAddMember").click(function ascuAddMember(){
        if(memberadd==1)
            adaugaMembruHide();
    })
    
    $("#delMembru").click(function afisDelMember(){
        if(memberdel==0)
            stergeMembruShow();
    });
    
    $(".cancelDelMember").click(function ascuDelMember(){
        if(memberdel==1)
            stergeMembruHide();
    })
    
     $("#addSemiAdm").click(function (){
        if(adminadd==0)
            adaugaAdminShow();
    });

    $(".cancelAddAdmin").click(function (){
        if(adminadd==1)
            adaugaAdminHide();
    })
    
    $("#delSemiAdm").click(function (){
        if(admindel==0)
            stergeAdminShow();
    });
    
    $(".cancelDelAdmin").click(function (){
        if(admindel==1)
            stergeAdminHide();
    })
    
    $("#delProiect").click(function afisDelProiect(){
        if(proiectdel==0)
            stergeProiectShow();
    });
    
    $(".cancelDelProiect").click(function ascuDelProiect(){
        if(proiectdel==1)
            stergeProiectHide();
    })
    
    $("#exportCSV").click(function afisDelProiect(){
        if(expraport==0)
            exportRaportShow();
    });
    
    $(".cancelExpRaport").click(function (){
        if(expraport==1)
            exportRaportHide();
    })
    
    $("#project_selector").change(function(){
        if($(this).val()=="adaugaProiect"){
            if(proiect==0)
                adaugaProiectFormShow();
        }
        else
        {
            if(proiect==1)
                adaugaProiectFormHide();
        }
        
        if($(this).val()!="adaugaProiect"&&$(this).val()!="")
        {
            this.form.submit();
        }
    })
})